''' Ques- Write a Python program that prompts the user to enter 
a temperature in Celsius and converts it to Fahrenheit using the 
formula: Fahrenheit = (Celsius * 9/5) + 32.'''

temp_celsius=float(input("Enter the Temperature in celsius: "))
temp_farenheit = (temp_celsius * 9/5) + 32
print(temp_farenheit)
