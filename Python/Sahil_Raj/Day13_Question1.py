#Create two sets, "set1" and "set2," with some common elements. Print the union of the two sets with a descriptive message.
# Creating set1 and set2
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

# Printing the union of set1 and set2
union_set = set1.union(set2)
